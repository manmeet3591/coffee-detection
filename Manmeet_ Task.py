    def _add_esri_fallback(self):
        self.m.add_basemap("Esri.WorldImagery")

    def _circle_aoi(self, lon, lat, radius_m=1000):
        return ee.Geometry.Point([lon, lat]).buffer(radius_m).bounds()

    def _nearest_emit_band_name(self, img, target_nm: float) -> ee.String:
        wl = ee.List(img.get("reflectance_wavelengths"))
        diffs = wl.map(lambda x: ee.Number(x).subtract(target_nm).abs())
        idx = diffs.indexOf(ee.Number(diffs.reduce(ee.Reducer.min())))
        return ee.String("reflectance_").cat(ee.Number(idx).format())

    def _handle_draw(self, target, action, geo_json):
        if action == "created":
            self.drawn_geoms.append(ee.Geometry(geo_json["geometry"]))
            print("Geometry captured.")
        elif action == "deleted":
            self.drawn_geoms.clear()
            print("All drawings cleared.")

    def _remove_layer_if_exists(self, layer_obj):
        if layer_obj is None:
            return
        try:
            self.m.remove_layer(layer_obj)
        except Exception:
            pass

    def _last_geom(self):
        return self.drawn_geoms[-1] if len(self.drawn_geoms) else None

